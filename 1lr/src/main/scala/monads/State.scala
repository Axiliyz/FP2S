package monads

// State[S, A] оборачивает функцию S => (S, A).
// Берёт старое состояние, возвращает НОВОЕ состояние и значение.
// Чистая функциональная модель мутабельного состояния — никаких var, никаких побочных эффектов.
// S в проекте = PostOfficeState.
final case class State[S, A](run: S => (S, A)):

  // Запускает вычисление, применяет f только к значению.
  // Состояние s1 передаётся без изменений.
  def map[B](f: A => B): State[S, B] =
    State { s =>
      val (s1, a) = run(s)
      (s1, f(a))
    }

  // Ключевой метод: состояние s1 от первого шага передаётся второму.
  // Это "цепочка обновлений" — каждый шаг видит актуальное состояние предыдущего.
  def flatMap[B](f: A => State[S, B]): State[S, B] =
    State { s =>
      val (s1, a) = run(s)   // первый шаг: s - (s1, a)
      f(a).run(s1)            // второй шаг получает обновлённое s1, а не исходное s
    }

object State:

  // Читает состояние как значение. Состояние не меняется (s остаётся),
  // но возвращается как A — можно использовать внутри for-comprehension.
  def get[S]: State[S, S] = State(s => (s, s))

  // Заменяет состояние целиком на s. Старое состояние игнорируется (_).
  def set[S](s: S): State[S, Unit] = State(_ => (s, ()))

  // Трансформирует состояние функцией f. Не заменяет полностью, а преобразует.
  def modify[S](f: S => S): State[S, Unit] = State(s => (f(s), ()))

  // Нейтральный элемент: состояние не меняется (s => (s, a)), просто возвращает a.
  def pure[S, A](a: A): State[S, A] = State(s => (s, a))

  // Читает часть состояния функцией f. Состояние остаётся неизменным.
  // Аналог Reader.asks, но для State.
  def inspect[S, A](f: S => A): State[S, A] =
    State(s => (s, f(s)))

  // given — свидетель что State[S, ?] является монадой.
  // [A] =>> State[S, A] — лямбда на уровне типов, фиксирует S, оставляет A свободным.
  given stateMonad[S]: Monad[[A] =>> State[S, A]] with
    def pure[A](a: A): State[S, A] = State.pure(a)
    def flatMap[A, B](ma: State[S, A])(f: A => State[S, B]): State[S, B] =
      ma.flatMap(f)

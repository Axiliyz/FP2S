package monads

// Type class — интерфейс, параметризованный над конструктором типов M[_].
// M[_] означает: M сам принимает один тип. Нельзя написать Monad[Int] —
// только Monad[IO], Monad[List], Monad[Reader[Cfg, ?]] и т.д.
trait Monad[M[_]]:

  // Поднимает чистое значение в монадический контекст без добавления эффектов.
  // Нейтральный элемент: pure(a).flatMap(f) == f(a)
  def pure[A](a: A): M[A]

  // Связывает два монадических вычисления.
  // ma — первое вычисление, f — следующий шаг, который сам производит эффект.
  // Именно здесь прячется специфика каждой монады: как комбинируются эффекты.
  def flatMap[A, B](ma: M[A])(f: A => M[B]): M[B]

  // map выводится из pure + flatMap — бесплатно для любой монады.
  // Любая монада автоматически является функтором.
  // Логика: войти в контекст, применить f, завернуть обратно через pure.
  def map[A, B](ma: M[A])(f: A => B): M[B] =
    flatMap(ma)(a => pure(f(a)))

object Monad:
  // Extension-методы для любого M[A], у которого есть given Monad[M].
  // Именно эти строки позволяют писать for-comprehension на всех монадах проекта:
  // компилятор ищет .flatMap на IO[A], находит этот extension, подставляет ioMonad.
  extension [M[_], A](ma: M[A])(using m: Monad[M])
    def flatMap[B](f: A => M[B]): M[B] = m.flatMap(ma)(f)
    def map[B](f: A => B): M[B]        = m.map(ma)(f)
